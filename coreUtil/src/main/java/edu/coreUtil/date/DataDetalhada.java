package edu.coreUtil.date;

/**
 * @author eduardo
 *
 */
public class DataDetalhada {

	private int ano;

	private int mes;

	private int dias;

	private int hora;

	private int Minuto;

	private int segundos;

	public int getAno(){
		return ano;
	}

	public void setAno(int ano){
		this.ano = ano;
	}

	public int getMes(){
		return mes;
	}

	public void setMes(int mes){
		this.mes = mes;
	}

	public int getDias(){
		return dias;
	}

	public void setDias(int dias){
		this.dias = dias;
	}

	public int getHora(){
		return hora;
	}

	public void setHora(int hora){
		this.hora = hora;
	}

	public int getMinuto(){
		return Minuto;
	}

	public void setMinuto(int minuto){
		Minuto = minuto;
	}

	public int getSegundos(){
		return segundos;
	}

	public void setSegundos(int segundos){
		this.segundos = segundos;
	}

}
